public class XCoordinate extends Coordinate {

	// Constructors

	/**
	 * Construct an XCoordinate with the given value
	 */
	public XCoordinate(int value) {
		super(value);
	}

	/**
	 * Construct a new XCoordinate with the same value as the given XCoordinate
	 */
	public XCoordinate(XCoordinate coordinate) {
		super(coordinate);
	}
	

}